// Add event listener to customize toggle
document.addEventListener('DOMContentLoaded', function () {
    const customizeStyleController = document.getElementById('customiseToggle');

    const enableCustomizeContainer = () => {
        const customizeContainer = document.getElementById('customize-container');
        if (customizeContainer) {
            customizeContainer.style.opacity = '1';
            customizeContainer.style.pointerEvents = 'auto';
            customizeContainer.style.userSelect = 'auto';
            customizeContainer.style.mozUserSelect = 'auto';
        }
    };

    const disableCustomizeContainer = () => {
        const customizeContainer = document.getElementById('customize-container');
        if (customizeContainer) {
            customizeContainer.style.opacity = '0.3';
            customizeContainer.style.pointerEvents = 'none';
            customizeContainer.style.userSelect = 'none';
            customizeContainer.style.mozUserSelect = 'none';
        }
    };

    // Check initial state of toggle
    let isChecked = document.getElementById('customiseToggle').checked;

    if (isChecked) {
        enableCustomizeContainer();
    } else {
        disableCustomizeContainer();
    }


    customizeStyleController.addEventListener('change', function () {
        let isChecked = document.getElementById('customiseToggle').checked;

        if (isChecked) {
            // Show customize container
            enableCustomizeContainer();
        } else {
            // Hide customize container
            disableCustomizeContainer();
        }
    });
});

